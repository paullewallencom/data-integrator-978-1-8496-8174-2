CREATE TABLE SRC_SALGRADE
(
  GRADE  NUMBER,
  LOSAL  NUMBER,
  HISAL  NUMBER
)
/


CREATE TABLE SRC_DEPT
(
  DEPTNO  NUMBER(2),
  DNAME   VARCHAR2(14 BYTE),
  LOC     VARCHAR2(13 BYTE)
)
/


CREATE TABLE SRC_BONUS
(
  ENAME  VARCHAR2(10 BYTE),
  JOB    VARCHAR2(9 BYTE),
  SAL    NUMBER,
  COMM   NUMBER
)
/




CREATE TABLE SRC_EMP
(
  EMPNO     NUMBER(4),
  ENAME     VARCHAR2(10 BYTE),
  JOB       VARCHAR2(9 BYTE),
  MGR       NUMBER(4),
  HIREDATE  DATE,
  SAL       NUMBER(7,2),
  COMM      NUMBER(7,2),
  DEPTNO    NUMBER(2)
)
/


CREATE UNIQUE INDEX PK_SRC_DEPT ON SRC_DEPT
(DEPTNO)
/

CREATE UNIQUE INDEX PK_SRC_EMP ON SRC_EMP
(EMPNO)
/



ALTER TABLE SRC_DEPT ADD (
  CONSTRAINT PK_SRC_DEPT
 PRIMARY KEY
 (DEPTNO))
/


ALTER TABLE SRC_EMP ADD (
  CONSTRAINT PK_SRC_EMP
 PRIMARY KEY
 (EMPNO))
/



ALTER TABLE SRC_EMP ADD (
  CONSTRAINT FK_DEPTNO 
 FOREIGN KEY (DEPTNO) 
 REFERENCES SRC_DEPT (DEPTNO))
/


